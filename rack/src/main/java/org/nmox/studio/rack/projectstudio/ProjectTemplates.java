    private final String displayName;
    private final String description;

    ProjectTemplates(String displayName, String description) {
        this.displayName = displayName;
        this.description = description;
    }

    public String getDisplayName() {
        return displayName;
    }

    public String getDescription() {
        return description;
    }

    abstract void writeFiles(Path dir, String name) throws IOException;

    abstract JSONObject buildPatch();

    /** Whether the generated project gets an eslint flat config. */
    boolean lintable() {
        return true;
    }

    /**
     * Generates the project into {@code dir} (created; must not already
     * contain files): sources, package.json, .gitignore, README, and the
     * pre-wired rack patch.
     */
    public void generate(File dir, String name) throws IOException {
        Path root = dir.toPath();
        Files.createDirectories(root);
        try (var entries = Files.list(root)) {
            if (entries.findAny().isPresent()) {
                throw new IOException("Directory is not empty: " + dir);
            }
        }
        writeFiles(root, name);
        write(root, ".gitignore", """
            node_modules/
            dist/
            coverage/
            .env
            """);
        write(root, ".editorconfig", """
            root = true

            [*]
            charset = utf-8
            end_of_line = lf
            insert_final_newline = true
            indent_style = space
            indent_size = 2
            trim_trailing_whitespace = true
            """);
        write(root, ".prettierrc", """
            {
              "singleQuote": true,
              "semi": true,
              "printWidth": 100
            }
            """);
        if (lintable()) {
            write(root, "eslint.config.mjs", """
                import js from '@eslint/js';
                import globals from 'globals';

                export default [
                  js.configs.recommended,
                  {
                    languageOptions: {
                      globals: { ...globals.browser, ...globals.node },
                    },
                  },
                  { ignores: ['dist/', 'node_modules/', 'coverage/'] },
                ];
                """);
        }
        write(root, "README.md", """
            # %s

            Created with NMOX Studio (%s template).

            The Task Rack infra for this project lives in `.nmoxrack.json`
            and mounts automatically when the rack aims here:

            - press INSTALL on CRATE (or run `npm install`) once
            - START on SURGE serves the project and opens the browser
            - arm REFLEX's WATCH switch for a save-driven test loop
            """.formatted(name, displayName));
        Files.writeString(root.resolve(RackIO.DEFAULT_FILENAME),
                buildPatch().toString(2), StandardCharsets.UTF_8);
    }

    static void write(Path root, String relative, String content) throws IOException {
        Path target = root.resolve(relative);
        Files.createDirectories(target.getParent());
        Files.writeString(target, content, StandardCharsets.UTF_8);
    }

    /** Shared patch for the Vite-based templates (React/Vue). */
    private static JSONObject vitePatch() {
        return buildPatchFrom(rack -> {
            RackDevice master = add(rack, DeviceType.MASTER, null);
            RackDevice reflex = add(rack, DeviceType.REFLEX, Map.of("armed", "false", "filter", "1"));
            RackDevice deps = add(rack, DeviceType.PACKAGE_MANAGER, null);
            // vite on 5173
            RackDevice server = add(rack, DeviceType.DEV_SERVER, Map.of("server", "1", "port", "2"));
            RackDevice browser = add(rack, DeviceType.BROWSER, Map.of("url", "http://localhost:5173"));
            RackDevice build = add(rack, DeviceType.BUILD, Map.of("tool", "1"));
            RackDevice test = add(rack, DeviceType.TEST, Map.of("framework", "2"));
            RackDevice console = add(rack, DeviceType.CONSOLE, null);

            // CI lane
            rack.connect(master.getPort("trig1"), deps.getPort("run"));
            rack.connect(deps.getPort("ok"), build.getPort("run"));
            rack.connect(build.getPort("ok"), test.getPort("run"));
            rack.connect(test.getPort("out"), console.getPort("in"));
            // dev lane
            rack.connect(server.getPort("url"), browser.getPort("url"));
            rack.connect(server.getPort("ready"), browser.getPort("open"));
            // TDD lane (arm REFLEX to activate)
            rack.connect(reflex.getPort("changed"), test.getPort("run"));
        });
    }
