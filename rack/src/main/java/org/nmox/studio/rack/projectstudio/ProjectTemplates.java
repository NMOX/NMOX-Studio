    /** my-app -> MyApp, for Elixir module names. */
    static String toModuleName(String name) {
        StringBuilder sb = new StringBuilder();
        for (String part : name.split("[-_\\s]+")) {
            if (!part.isEmpty()) {
                sb.append(Character.toUpperCase(part.charAt(0))).append(part.substring(1));
            }
        }
        return sb.isEmpty() ? "App" : sb.toString();
    }

    /** my-app -> my_app, for Elixir app atoms and file names. */
    static String snake(String name) {
        return name.toLowerCase().replaceAll("[-\\s]+", "_").replaceAll("[^a-z0-9_]", "");
    }

