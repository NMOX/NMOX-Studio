                RackDevice typecheck = add(rack, DeviceType.TYPECHECK, null);
                RackDevice build = add(rack, DeviceType.BUILD, null);
                RackDevice test = add(rack, DeviceType.TEST, Map.of("framework", "2"));
                RackDevice console = add(rack, DeviceType.CONSOLE, null);
                // save -> typecheck -> tests; CI: install -> build -> tests
                rack.connect(reflex.getPort("changed"), typecheck.getPort("run"));
