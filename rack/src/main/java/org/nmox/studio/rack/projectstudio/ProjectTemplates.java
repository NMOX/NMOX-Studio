    /**
     * Generates the project into {@code dir} (created; must not already
     * contain files): sources, package.json, .gitignore, README, and the
     * pre-wired rack patch.
     */
    public void generate(File dir, String name) throws IOException {
        Path root = dir.toPath();
        Files.createDirectories(root);
        try (var entries = Files.list(root)) {
            if (entries.findAny().isPresent()) {
                throw new IOException("Directory is not empty: " + dir);
            }
        }
        writeFiles(root, name);
        write(root, ".gitignore", gitignore());
        write(root, ".editorconfig", """
            root = true

            [*]
            charset = utf-8
            end_of_line = lf
            insert_final_newline = true
            indent_style = space
            indent_size = 2
            trim_trailing_whitespace = true
            """);
        if (prettier()) {
            write(root, ".prettierrc", """
                {
                  "singleQuote": true,
                  "semi": true,
                  "printWidth": 100
                }
                """);
        }
        if (lintable()) {
            write(root, "eslint.config.mjs", """
                import js from '@eslint/js';
                import globals from 'globals';

                export default [
                  js.configs.recommended,
                  {
                    languageOptions: {
                      globals: { ...globals.browser, ...globals.node },
                    },
                  },
                  { ignores: ['dist/', 'node_modules/', 'coverage/'] },
                ];
                """);
        }
        write(root, "README.md", """
            # %s

            Created with NMOX Studio (%s template).

            The Task Rack infra for this project lives in `.nmoxrack.json`
            and mounts automatically when the rack aims here:

%s""".formatted(name, displayName, readmeHints()));
        Files.writeString(root.resolve(RackIO.DEFAULT_FILENAME),
                buildPatch().toString(2), StandardCharsets.UTF_8);
    }

    static void write(Path root, String relative, String content) throws IOException {
        Path target = root.resolve(relative);
        Files.createDirectories(target.getParent());
        Files.writeString(target, content, StandardCharsets.UTF_8);
    }

